#include "../includes/libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*new;

	if (!(new = malloc(sizeof(t_list))))
		return (NULL);
	if (!content)
	{
		new->content = 0;
		new->content_size = 0;
	}
	else
	{
		new->content = (void*)content;
		new->content_size = content_size;
	}
	new->next = NULL;
	return (new);
}
