#include "../includes/libft.h"

int	ft_sqrt(int nb)
{
	int	res;
	int	i;

	res = 0;
	i = 1 << 30;
	while (i > nb)
		i >>= 2;
	while (i != 0)
	{
		if (nb >= res + i)
		{
			nb -= res + i;
			res = (res >> 1) + i;
		}
		else
			res >>= 1;
		i >>= 2;
	}
	return (res);
}
